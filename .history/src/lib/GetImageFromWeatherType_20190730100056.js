import {theme} from '../theme';

export const getImageFromWeatherType = (weatherType) => {
    switch (weatherType) {
        case "cloud":
            return theme.images.cloud;
        case "rain":
            return theme.images.rain;
        case "cloud":
            return theme.images.cloud;
        case "cloud":
            return theme.images.cloud;
        case "cloud":
            return theme.images.cloud;
        case "cloud":
            return theme.images.cloud;
        
    }
}
